package mx.itesm.fitnesstracker.model;

public class Goal {

	private int minutes;

	public Goal() {
		super();
	}
	
	public Goal(int minutes) {
		super();
		this.minutes = minutes;
	}

	public int getMinutes() {
		return minutes;
	}

	public void setMinutes(int minutes) {
		this.minutes = minutes;
	}
}
